-- Этот скрипт выполняется автоматически при ПЕРВОМ старте контейнера postgres
-- (когда каталог /var/lib/postgresql/data пуст). Подключается через монтирование
-- в /docker-entrypoint-initdb.d/ — см. docker-compose.yml.
--
-- При повторных запусках скрипт НЕ выполняется. Чтобы переинициализировать
-- кластер: `docker compose down -v && docker compose up -d postgres`
-- (внимание: это удалит все данные).
--
-- Скрипт выполняется суперпользователем (POSTGRES_USER) в базе POSTGRES_DB.
-- =============================================================================

-- Расширение pgvector — добавляет тип VECTOR и операторы расстояния (<->, <#>, <=>)
-- для поиска ближайших соседей (KNN). Используется для хранения эмбеддингов
-- документов и статей базы знаний (см. ТЗ §4.5 и §5: Documents.embedding,
-- KnowledgeBase.embedding).
-- IF NOT EXISTS — на случай, если кто-то вручную добавит расширение раньше.
CREATE EXTENSION IF NOT EXISTS vector;

-- pg_trgm — триграммные индексы для быстрого полнотекстового поиска подстрок
-- (ILIKE '%...%'). Полезен для поиска по названиям договоров, ФИО и т.п.
-- параллельно с основным FTS на tsvector.
CREATE EXTENSION IF NOT EXISTS pg_trgm;

-- uuid-ossp — генерация UUID v4 на стороне БД (uuid_generate_v4()).
-- Все первичные ключи в модели данных (ТЗ §5) имеют тип UUID.
-- Альтернатива — gen_random_uuid() из pgcrypto, тоже подходит; Prisma умеет обе.
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- Жёстко фиксируем таймзону на уровне БД: NOW(), CURRENT_TIMESTAMP, конвертация
-- timestamptz и триггеры теперь работают в Asia/Irkutsk независимо от env TZ
-- контейнера и от настроек клиента. Для юридического ПО это критично —
-- временные метки (сроки, дедлайны, аудит) должны быть детерминированы.
-- ALTER DATABASE меняет параметр на уровне базы; применяется при новом
-- подключении. Имя БД берём из current_database(), чтобы скрипт не зависел
-- от значения POSTGRES_DB (docker-entrypoint-initdb.d подключает psql без
-- проброса переменных окружения в psql-переменные).
DO $$
BEGIN
  EXECUTE format('ALTER DATABASE %I SET timezone TO %L',
                 current_database(), 'Asia/Irkutsk');
END
$$;
